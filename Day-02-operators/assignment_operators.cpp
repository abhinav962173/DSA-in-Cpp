#include <iostream>
using namespace std;

int main(){
    //  Assignment Operators


    int a = 10;

    cout << "a = " << a  << endl;           // a = 10 
    cout << "a += 5  -> " << a + 5  << endl;     // a = a + 5 -> 15
    cout << "a -= 1  -> " << a - 1  << endl;      // a = a - 1 -> 9
    cout << "a *= 10  -> " << a * 10 << endl;      // a = a * 10 -> 100
    cout << "a /= 2  -> " << a / 2 << endl;       // a = a / 2 -> 5
    cout << "a %= 2  -> " << a % 2 << endl;       // a = a % 2 -> 0
    
   

    return 0;
}