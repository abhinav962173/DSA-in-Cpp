#include <iostream>
using namespace std;

int main(){
    //  Logical Operators   (True / false)

    int a = 10;
    int b = 15;

    cout << (a == b) <<endl;    // 0 -> false 
    cout << (a != b) <<endl;    // 1 -> true
    cout << (a >= b) <<endl;    // 0 -> false
    cout << (a <= b) <<endl;    // 1 -> true
    cout << (a == b) <<endl;
    
   

    return 0;
}